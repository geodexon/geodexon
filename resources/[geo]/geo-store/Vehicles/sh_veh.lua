exports('GetCarInfo', function()
    local names = {}
    for k,v in pairs(VehiclePrices) do
        names[GetLabelText(GetDisplayNameFromVehicleModel(GetHashKey(k)))] = v
    end

    local newNames = {}
    for k,v in Shared.SortAlphabet(names) do
        table.insert(newNames, {k, v})
    end

    return newNames
end)

AddEventHandler('LocateCar', function(name)
    for k,v in pairs(VehiclePrices) do
        if GetLabelText(GetDisplayNameFromVehicleModel(GetHashKey(k))) == name then
            local pos = Task.Run('FindCar', k)
            if pos then
                SetNewWaypoint(pos.x, pos.y)
                TriggerEvent('Shared.Notif', 'Waypoint set', 5000)
            else
                TriggerEvent('Shared.Notif', 'This car is not stocked anywhere right now', 5000)
            end
            break
        end
    end
end)